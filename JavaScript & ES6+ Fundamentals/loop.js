// Loops are used to execute a block of code repeatedly
// until a specified condition is met.

// for loop – runs code a fixed number of times
for (let i = 1; i <= 5; i++) {
  console.log(i);
}

// while loop – runs code while the condition is true
let j = 1;
while (j <= 5) {
  console.log(j);
  j++;
}
