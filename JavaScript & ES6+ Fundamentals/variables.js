// Var 
// 1.Function scoped (not block scoped)
// 2.Can be re-declared
// 3.Can be re-assigned
// 4.Hoisted and initialized with undefined 

var firstname = "Raj"
console.log(firstname)


// let 
// 1.block scoped
// 2.Can not be re-declared
// 3.Can be re-assigned
// 4.Hoisted and initialized with reference error

let middleName = "Ramkishor"
console.log(middleName)

// const 
// 1.block scoped
// 2.Can not be re-declared
// 3.Can be re-assigned
// 4.Hoisted and initialized with reference error

const lastName = "Keshkar"
console.log(lastName)








