// if–else–if is used to check multiple conditions in JavaScript.
// The first true condition’s block will be executed.
// If none of the conditions are true, the else block runs.

let marks = 75

if (marks >= 90) {
  console.log("Grade: A")
} else if (marks >= 60) {
  console.log("Grade: B")
} else {
  console.log("Grade: C")
}

